const MORSE_TABLE = {
    '.-': 'a',
    '-...': 'b',
    '-.-.': 'c',
    '-..': 'd',
    '.': 'e',
    '..-.': 'f',
    '--.': 'g',
    '....': 'h',
    '..': 'i',
    '.---': 'j',
    '-.-': 'k',
    '.-..': 'l',
    '--': 'm',
    '-.': 'n',
    '---': 'o',
    '.--.': 'p',
    '--.-': 'q',
    '.-.': 'r',
    '...': 's',
    '-': 't',
    '..-': 'u',
    '...-': 'v',
    '.--': 'w',
    '-..-': 'x',
    '-.--': 'y',
    '--..': 'z',
    '.----': '1',
    '..---': '2',
    '...--': '3',
    '....-': '4',
    '.....': '5',
    '-....': '6',
    '--...': '7',
    '---..': '8',
    '----.': '9',
    '-----': '0',
};

function decode(expr) {

    let letterArr = expr.match(/.{1,10}/g, 10);
    let morseLetters = letterArr.map(item => item.replace(/10/g, '.').replace(/11/g, '-').replace(/\*+/g, ' ').replace(/0/g, ''));
    let morseString = morseLetters.map(item => {
        let char = MORSE_TABLE[item];
        return (char === undefined) ? ' ' : char;
    })
    return morseString.join('');
}

module.exports = {
    decode
}